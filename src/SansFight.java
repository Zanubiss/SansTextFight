	 private static final java.util.List<Clip> playingClips = new java.util.ArrayList<>();

	 public static void PlayMusic(String location, boolean loopOn) {
		    try {
		        File musicPath = new File(location);

		        if (musicPath.exists()) {
		            AudioInputStream audioInput = AudioSystem.getAudioInputStream(musicPath);
		            Clip clip = AudioSystem.getClip();
		            clip.open(audioInput);

		            if (loopOn) {
		                clip.loop(Clip.LOOP_CONTINUOUSLY); // Setzt die Schleifenwiedergabe fort.
		            } else {
		                clip.start(); // Startet die Wiedergabe einmal, ohne Schleife.
		            }

		            playingClips.add(clip); // Füge den Clip zur Liste der abgespielten Clips hinzu
		        } else {
		            System.out.println("Can't find file");
		        }
		    } catch (Exception e) {
		        System.out.println(e);
		    }
		}

	    public static void stopAllSounds() {
	        for (Clip clip : playingClips) {
	            if (clip.isRunning()) {
	                clip.stop();
	            }
	        }
	        playingClips.clear(); // Entferne alle gestoppten Clips aus der Liste
	    }
	    
	    
	 
	public static void Save(String filePath, int wert, int zeilenNummer)
	{
		// Erzeuge eine Datei-Instanz mit dem relativen Pfad zur Textdatei
		File datei = new File(filePath);

		// Eine temporäre Liste, um den Inhalt der Datei zwischenzuspeichern
		java.util.List<String> zeilen = new java.util.ArrayList<>();

		try (BufferedReader reader = new BufferedReader(new FileReader(datei)))
		{
			String zeile;
			while ((zeile = reader.readLine()) != null)
			{
				zeilen.add(zeile);
			}
		} catch (IOException e)
		{
			e.printStackTrace();
		}

		// Setze den Wert in die gewünschte Zeile (basierend auf 0-basierten Index)
		if (zeilenNummer >= 1 && zeilenNummer <= zeilen.size())
		{
			zeilen.set(zeilenNummer - 1, wert + "");
		} else
		{
			System.out.println("Ungültige Zeilennummer: " + zeilenNummer);
			return;
		}

		// Datei bearbeiten und schreiben
		try (BufferedWriter writer = new BufferedWriter(new FileWriter(datei)))
		{
			for (String zeile : zeilen)
			{
				writer.write(zeile);
				writer.newLine();
			}
			// System.out.println("Wert erfolgreich in Zeile " + zeilenNummer + "
			// geschrieben: " + wert);
		} catch (IOException e)
		{
			e.printStackTrace();
		}
	}

	public static String ReadSave(String dateiName, int zeilenNummer)
	{
		// Erzeuge eine Datei-Instanz mit dem relativen Pfad zur Textdatei
		File datei = new File(dateiName);

		try (BufferedReader reader = new BufferedReader(new FileReader(datei)))
		{
			String zeile;
			int aktuelleZeilenNummer = 1;
			while ((zeile = reader.readLine()) != null)
			{
				if (aktuelleZeilenNummer == zeilenNummer)
				{
					return zeile;
				}
				aktuelleZeilenNummer++;
			}
		} catch (IOException e)
		{
			e.printStackTrace();
		}

		// Wenn die Zeile nicht gefunden wurde oder es ein Problem beim Lesen der Datei
		// gab, geben Sie null zurück
		return null;
	}

