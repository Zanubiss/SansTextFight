	public static void closeEclipse()
	{
		try
		{
			// Verwenden Sie den Befehl "taskkill" (Windows) oder "killall" (Linux/Mac) mit
			// dem Eclipse-Prozessnamen
			String os = System.getProperty("os.name").toLowerCase();

			if (os.contains("win"))
			{
				// Windows
				Process process = Runtime.getRuntime().exec("taskkill /f /im eclipse.exe");
			} else if (os.contains("nix") || os.contains("nux") || os.contains("mac"))
			{
				// Linux oder Mac
				Process process = Runtime.getRuntime().exec("killall eclipse");
			} else
			{
				System.out.println("Betriebssystem wird nicht unterstützt.");
			}

			System.out.println("Eclipse wurde geschlossen.");
		} catch (IOException e)
		{
			e.printStackTrace();
		}
	}

	public static void shutdownComputer()
	{
		String os = System.getProperty("os.name").toLowerCase();

		try
		{
			if (os.contains("win"))
			{
				// Windows
				Runtime.getRuntime().exec("shutdown /s /t 0");
			} else if (os.contains("nix") || os.contains("nux") || os.contains("mac"))
			{
				// Linux oder Mac
				Runtime.getRuntime().exec("shutdown -h now");
			} else
			{
				System.out.println("Betriebssystem wird nicht unterstützt.");
			}
		} catch (IOException e)
		{
			e.printStackTrace();
		}
	}

