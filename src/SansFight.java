	public static void zeilenAusgabe(String text)
	{
		int startIndex = 0;
		int endIndex;

		while ((endIndex = text.indexOf("\n", startIndex)) != -1)
		{
			String zeile = text.substring(startIndex, endIndex);
			System.out.println(zeile);
			try
			{
				Thread.sleep(7);
			} catch (InterruptedException e)
			{
				e.printStackTrace();
			}
			startIndex = endIndex + 2; // 2, um das \n zu überspringen
		}

		// Die letzte Zeile (oder die einzige Zeile, wenn kein \n gefunden wurde)
		String letzteZeile = text.substring(startIndex);
		System.out.println(letzteZeile);
	}

	public static void schrittweiseAusgabe(String text, String soundFilePath)
	{
		StringBuilder ausgabe = new StringBuilder();
		int pauseMillis = 50; // Standardpause
		

		

		for (int i = 0; i < text.length(); i++)
		{
			char currentChar = text.charAt(i);
			ausgabe.append(currentChar); // Füge das aktuelle Zeichen zur Ausgabe hinzu
			System.out.print(currentChar); // Gib das aktuelle Zeichen aus

			if (currentChar == '\n')
			{
				// Wenn ein Zeilenumbruch (\n) gefunden wurde, erhöhe die Pause
				pauseMillis = 1000; // Längere Pause nach Zeilenumbruch (1000) Millisekunden)
				i++; // Überspringe das '\n'-Zeichen
			} else if (currentChar != ' ') // Überprüfe, ob es sich nicht um ein Leerzeichen handelt
			{	
		
				pauseMillis = 50; // Standardpause für andere Zeichen
				if (text == intro2)
				{
					 pauseMillis = 150;
				}
				
				
				PlayMusic(soundFilePath, false); // Verwende den übergebenen Sound
			}

			try
			{
				Thread.sleep(pauseMillis); // Füge die entsprechende Verzögerung ein
			} catch (InterruptedException e)
			{
				e.printStackTrace();
			}
		}
	}

